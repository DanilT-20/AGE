from turtle import *
m = 40
tracer(0)
screensize(10000, 10000)
for x in range(3):
    fd(2*m)
    rt(90)
    fd(3*m)
    lt(90)
rt(180)
fd(6*m)
rt(90)
fd(9*m)
up()
back(3*m)
rt(90)
down()
for x in range(2):
    fd(1*m)
    rt(90)
    fd(2*m)
    lt(90)
rt(180)
fd(3*m)
rt(90)
fd(4*m)
rt(90)
fd(m)
up()
for x in range(-40,40):
    for y in range(-40,40):
        goto(x*m, y*m)
        dot(3, 'white')
update()
done()